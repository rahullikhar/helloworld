#operators
# Basic exponentiation
result = 2 ** 3  # Calculate 2 to the power of 3
print(result)

# Exponentiation with a negative exponent
result = 4 ** (-2) # Calculate 4 to the power of -2 (which is 1/16)
print(result)

# Exponentiation with a fractional exponent (square root)
result = 25 ** 0.5  # Calculate the square root of 25
print(result)